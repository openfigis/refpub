package org.fao.fi.refpub.dao.objects.chunks;

public class MDConcept {
	private String table_name;
	private String table_group;
	private String rest_concept;
	private String fic_meta;
	public String getTable_name() {
		return table_name;
	}
	public void setTable_name(String table_name) {
		this.table_name = table_name;
	}
	public String getTable_group() {
		return table_group;
	}
	public void setTable_group(String table_group) {
		this.table_group = table_group;
	}
	public String getRest_concept() {
		return rest_concept;
	}
	public void setRest_concept(String rest_concept) {
		this.rest_concept = rest_concept;
	}
	public String getFic_meta() {
		return fic_meta;
	}
	public void setFic_meta(String fic_meta) {
		this.fic_meta = fic_meta;
	}
}
